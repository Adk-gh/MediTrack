const { z } = require("zod");

const surgicalHistorySchema = z.object({
  name: z.string().optional(),
  date: z.string().optional(),
});

const logEntrySchema = z.object({
  date: z.string().optional(),
  vitals: z.string().optional(),
  bodyMeasure: z.string().optional(),
  staff: z.string().optional(),
  remarks: z.string().optional(),
});

const createExaminationSchema = z.object({
  patientId: z.string().min(1, "Patient ID is required"),
  lastName: z.string().min(1, "Last name is required"),
  firstName: z.string().min(1, "First name is required"),
  middleName: z.string().optional(),
  suffix: z.string().optional(),
  studentId: z.string().optional(),
  schoolYear: z.string().optional(),
  course: z.string().optional(),
  yearSection: z.string().optional(),
  address: z.string().optional(),
  birthday: z.string().optional(),
  gender: z.enum(["Male", "Female"]).optional(),
  age: z.number().int().optional(),
  contactNo: z.string().optional(),
  email: z.string().email("Invalid email format").optional().or(z.literal("")),
  civilStatus: z.enum(["Single", "Married", "Widowed", "Divorced"]).optional(),
  emergencyName: z.string().optional(),
  emergencyRelation: z.string().optional(),
  emergencyPhone: z.string().optional(),
  medicalConditions: z.array(z.string()).optional(),
  surgicalHistory: z.array(surgicalHistorySchema).optional(),
  bp: z.string().optional(),
  pr: z.string().optional(),
  rr: z.string().optional(),
  temp: z.string().optional(),
  wt: z.string().optional(),
  ht: z.string().optional(),
  waist: z.string().optional(),
  lmp: z.string().optional(),
  nurse: z.string().optional(),
  physician: z.string().optional(),
  remarks: z.string().optional(),
  logs: z.array(logEntrySchema).optional(),
});

const updateExaminationSchema = z.object({
  lastName: z.string().optional(),
  firstName: z.string().optional(),
  middleName: z.string().optional(),
  suffix: z.string().optional(),
  course: z.string().optional(),
  yearSection: z.string().optional(),
  address: z.string().optional(),
  birthday: z.string().optional(),
  gender: z.enum(["Male", "Female"]).optional(),
  age: z.number().int().optional(),
  contactNo: z.string().optional(),
  email: z.string().email("Invalid email format").optional().or(z.literal("")),
  civilStatus: z.enum(["Single", "Married", "Widowed", "Divorced"]).optional(),
  emergencyName: z.string().optional(),
  emergencyRelation: z.string().optional(),
  emergencyPhone: z.string().optional(),
  medicalConditions: z.array(z.string()).optional(),
  surgicalHistory: z.array(surgicalHistorySchema).optional(),
  bp: z.string().optional(),
  pr: z.string().optional(),
  rr: z.string().optional(),
  temp: z.string().optional(),
  wt: z.string().optional(),
  ht: z.string().optional(),
  waist: z.string().optional(),
  lmp: z.string().optional(),
  nurse: z.string().optional(),
  physician: z.string().optional(),
  remarks: z.string().optional(),
  logs: z.array(logEntrySchema).optional(),
});

module.exports = { createExaminationSchema, updateExaminationSchema };